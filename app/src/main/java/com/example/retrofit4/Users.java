package com.example.retrofit4;

public class Users {
    private String email;
    private int id;
    private String name;
    private String password;
    private String school;

    public String getEmail() {
        return email;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getSchool() {
        return school;
    }
}
